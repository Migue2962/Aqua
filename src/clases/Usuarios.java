package clases;

public class Usuarios {
    private String correo, nombre, apellido, apodo, contraseña, confcontraseña;

    public Usuarios() {
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getApodo() {
        return apodo;
    }

    public void setApodo(String apodo) {
        this.apodo = apodo;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getConfcontraseña() {
        return confcontraseña;
    }

    public void setConfcontraseña(String confcontraseña) {
        this.confcontraseña = confcontraseña;
    }
    
}
