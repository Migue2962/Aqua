package clases;
public class Agua{
    private int cons;
    private String importancia, significado, funciones, carcteristicas;

    public Agua() {
        
    }
    

    public int getCons() {
        return cons;
    }


    public void setCons(int cons) {
        this.cons = cons;
    }


    public String getImportancia() {
        return importancia;
    }

    public void setImportancia(String importancia) {
        this.importancia = importancia;
    }

    public String getsignificado() {
        return significado;
    }

    public void setsignificado (String significado) {
        this.significado = significado;
    }

    public String getFunciones() {
        return funciones;
    }

    public void setFunciones(String funciones) {
        this.funciones = funciones;
    }

    public String getCarcteristicas() {
        return carcteristicas;
    }

    public void setCarcteristicas(String carcteristicas) {
        this.carcteristicas = carcteristicas;
    }
    
    
}